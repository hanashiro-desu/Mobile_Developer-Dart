void main() {
  // Kiểu dữ liệu bool: chỉ nhận giá trị true hoặc false
  bool x = true; // Khai báo biến x có kiểu bool và gán giá trị là true

  // Kiểm tra giá trị của x
  if (x) { 
    // Nếu x là true, thực hiện lệnh trong khối này
    print("OK"); // In ra "OK"
  } else {
    // Nếu x là false, thực hiện lệnh trong khối này
    print("KO OK"); // In ra "KO OK"
  }
}